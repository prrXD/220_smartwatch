/* ==================== Chinese Character Rendering (16x16, GB2312) ==================== */

/**
  * @brief  Look up a Chinese character in OLED_CF16x16 by its GB2312 2-byte code.
  * @param  gb2312_code: pointer to 2-byte GB2312 code (e.g., "\xca\xb1" for 时)
  * @return pointer to ChineseCell_t, or NULL if not found
  */
static const ChineseCell_t* OLED_FindCN(const char *gb2312_code) {
    uint8_t count = OLED_CF16x16_Count;
    for (uint8_t i = 0; i < count; i++) {
        if (OLED_CF16x16[i].Index[0] == gb2312_code[0] &&
            OLED_CF16x16[i].Index[1] == gb2312_code[1]) {
            return &OLED_CF16x16[i];
        }
    }
    /* Return last entry as fallback (not-found glyph) */
    if (count > 0) return &OLED_CF16x16[count - 1];
    return 0;
}

/**
  * @brief  Draw a 16x16 Chinese character at (x, y).
  * @param  x: horizontal position (0-127)
  * @param  y: vertical position (0-63)
  * @param  gb2312_code: 2-byte GB2312 code string
  */
void OLED_DrawCNChar16(uint8_t x, uint8_t y, const char *gb2312_code) {
    if (x + 16 > SSD1306_WIDTH || y + 16 > SSD1306_HEIGHT) return;

    const ChineseCell_t *cell = OLED_FindCN(gb2312_code);
    if (!cell) return;

    const uint8_t *glyph = cell->Data;
    uint8_t start_page = y / 8;

    for (uint8_t page = 0; page < 2; page++) {
        if (start_page + page >= SSD1306_PAGES) break;
        for (uint8_t col = 0; col < 16; col++) {
            uint16_t offset = x + col + (start_page + page) * SSD1306_WIDTH;
            if (offset < sizeof(OLED_Buffer)) {
                uint8_t data = glyph[page * 16 + col];
                uint8_t bit_shift = y & 7;
                if (bit_shift) {
                    OLED_Buffer[offset] |= (data << bit_shift);
                    if (offset + SSD1306_WIDTH < sizeof(OLED_Buffer))
                        OLED_Buffer[offset + SSD1306_WIDTH] |= (data >> (8 - bit_shift));
                } else {
                    OLED_Buffer[offset] = data;
                }
            }
        }
    }
}

/**
  * @brief  Draw a string of GB2312-encoded Chinese characters.
  * @param  x: horizontal start position
  * @param  y: vertical start position
  * @param  gb2312_str: GB2312 encoded string (each char = 2 bytes)
  * @note   Each character is 16x16 pixels and advances x by 16.
  */
void OLED_DrawStringCN16(uint8_t x, uint8_t y, const char *gb2312_str) {
    while (*gb2312_str) {
        OLED_DrawCNChar16(x, y, gb2312_str);
        x += 16;
        if (x + 16 > SSD1306_WIDTH) break;
        gb2312_str += 2;  /* Each GB2312 char = 2 bytes */
    }
}